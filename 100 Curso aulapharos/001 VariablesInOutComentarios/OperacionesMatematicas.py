# Operaciones matemáticas
from math import ceil
from math import floor
numero1 = 4.5576576
numero2 = 5.5

# Multiplicación
numero = numero1 * numero2
print(numero)

# División
numero = numero1 / numero2
print(numero)

# Suma
numero = numero1 + numero2
print(numero)

# Resta
numero = numero1 - numero2
print(numero)

# Potencia
numero = numero1 ** numero2
print(numero)

# Parte entera de una división
numero = numero1 // numero2
print(numero)

# Resto de una división
numero = numero1 % numero2
print(numero)

# Redondear un número
round(numero2)
print(numero)

# Redondear a una serie de decimales
decimales = 2
print(numero)

round(numero2, decimales)
print(numero)

# Truncar
floor(numero2)
print(numero)

# Redondear al número superior
ceil(numero2)
print(numero)
